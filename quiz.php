<script type="text/javascript">
jQuery(document).ready(function($){

	//progress bar.
	var qprogress = 1;

	function updateProgressBar() {
		var t = qprogress;
		$("#qp" + t).addClass('qp-done')
		$("#qp" + t).removeClass('qp-active')
		t = qprogress = qprogress + 1;
		$("#qp" + t).addClass('qp-active')
	}

	$("#quiz-form").submit(function(event){

		  event.preventDefault();
		  $("#submit-btn").hide();
		  $("#ajax-error").hide();
		  $("#ajax-loading").show();
		  $.post( "process.php", $(this).serialize(),
			function(ajaxdata){

				if(ajaxdata['result'] == "OK") {
					if(qprogress == 7)
					window.location = ajaxdata.redirect_url;
					else {
						$("#ajax-content").html(ajaxdata.html);
						updateProgressBar();
						$("#ajax-loading").hide();
						$("#submit-btn").show();
					}
				}
				else if(ajaxdata['result'] == "ERROR") {
					$("#ajax-error").html(ajaxdata['msg']);
					$("#ajax-error").show();
					$("#ajax-loading").hide();
					$("#submit-btn").show();
				}
				else {
					$("#ajax-error").html("Unexpected error occured, Please try again.");
					$("#ajax-error").show();
					$("#ajax-loading").hide();
					$("#submit-btn").show();
				}


			}, 'json');


		});

	});
</script>
